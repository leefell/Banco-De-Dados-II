SELECT P.DESCRICAO,
	P.UNIDADE,
	IP.QUANTIDADE
FROM PRODUTO P
LEFT JOIN ITEM_PEDIDO IP ON P.CODIGO_PRODUTO = IP.CODIGO_PRODUTO;


SELECT P.DESCRICAO,
	P.UNIDADE,
	IP.QUANTIDADE
FROM PRODUTO P
LEFT JOIN ITEM_PEDIDO IP ON P.CODIGO_PRODUTO = IP.CODIGO_PRODUTO
WHERE IP.QUANTIDADE IS NULL;

