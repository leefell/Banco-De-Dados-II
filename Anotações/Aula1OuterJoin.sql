SELECT P.DESCRICAO,
	P.UNIDADE,
	IP.QUANTIDADE
FROM ITEM_PEDIDO IP
RIGHT OUTER JOIN PRODUTO P ON P.CODIGO_PRODUTO = IP.CODIGO_PRODUTO;


SELECT C.NOME_CLIENTE,
	P.PRAZO_ENTREGA
FROM PEDIDO P
RIGHT OUTER JOIN CLIENTE C ON P.CODIGO_CLIENTE = C.CODIGO_CLIENTE;


SELECT V.NOME_VENDEDOR,
	P.PRAZO_ENTREGA
FROM PEDIDO P
RIGHT OUTER JOIN VENDEDOR V ON P.CODIGO_VENDEDOR = V.CODIGO_VENDEDOR;

